public class AritmetickeOperacije {
    public static void main(String[] args) {

        int broj1 = 20;
        int broj2 = 4;

        // Sabiranje
        int zbir = broj1 + broj2;
        System.out.println("Zbir: " + zbir);

        // Oduzimanje
        int razlika = broj1 - broj2;
        System.out.println("Razlika: " + razlika);

        // Množenje
        int proizvod = broj1 * broj2;
        System.out.println("Proizvod: " + proizvod);

        // Deljenje
        int kolicnik = broj1 / broj2;
        System.out.println("Količnik: " + kolicnik);

        // Modul
        int ostatak = broj1 % broj2;
        System.out.println("Ostatak pri deljenju: " + ostatak);
    }
}

